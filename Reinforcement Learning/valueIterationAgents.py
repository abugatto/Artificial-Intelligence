class ValueIterationAgent(ValueEstimationAgent):
    """
        * Please read learningAgents.py before reading this.*

        A ValueIterationAgent takes a Markov decision process
        (see mdp.py) on initialization and runs value iteration
        for a given number of iterations using the supplied
        discount factor.
    """
    def __init__(self, mdp, discount = 0.9, iterations = 100):
        """
          Your value iteration agent should take an mdp on
          construction, run the indicated number of iterations
          and then act according to the resulting policy.

          Some useful mdp methods you will use:
              mdp.getStates()
              mdp.getPossibleActions(state)
              mdp.getTransitionStatesAndProbs(state, action)
              mdp.getReward(state, action, nextState)
              mdp.isTerminal(state)
        """
        self.mdp = mdp
        self.discount = discount
        self.iterations = iterations
        self.values = util.Counter() # A Counter is a dict with default 0
        self.runValueIteration()

    def runValueIteration(self): 
        for i in range(self.iterations): #dynamic programming
          newValues = self.values.copy() #need to recopy every iteration

          for state in self.mdp.getStates():
            if not self.mdp.isTerminal(state):
              QValues = [float("-inf")] #because we can have values below 0

              for action in self.mdp.getPossibleActions(state):
                QValues += [self.computeQValueFromValues(state, action)] #creates list of all possible Qvalues

              newValues[state] = max(QValues)

          self.values = newValues #repeats for i iterations in batch

    def getValue(self, state):
        """
          Return the value of the state (computed in __init__).
        """
        return self.values[state]


    def computeQValueFromValues(self, state, action):
        """
          Compute the Q-value of action in state from the
          value function stored in self.values.
        """

        #using Bellman Equation: Q(s,a) = SUM_s'(T(s,a,s')*[R(s,a,s') + gamma*Value(s')])
        QValue = 0
        for newState, transition in self.mdp.getTransitionStatesAndProbs(state, action):
          QValue += transition * (self.mdp.getReward(state, action, newState) + (self.discount * self.getValue(newState)))

        return QValue


    def computeActionFromValues(self, state):
        """
          The policy is the best action in the given state
          according to the values currently stored in self.values.

          You may break ties any way you see fit.  Note that if
          there are no legal actions, which is the case at the
          terminal state, you should return None.
        """

        avPair = [(float("-inf"), None)]
        for action in self.mdp.getPossibleActions(state): #create list of possible action-value pairs
          avPair += [(self.computeQValueFromValues(state, action), action)]

        return max(avPair)[1] #returns action from optimal action-value pair

    def getPolicy(self, state):
        return self.computeActionFromValues(state)

    def getAction(self, state):
        "Returns the policy at the state (no exploration)."
        return self.computeActionFromValues(state)

    def getQValue(self, state, action):
        return self.computeQValueFromValues(state, action)

